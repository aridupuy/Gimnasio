    public String consulta (String sql, String campo) throws SQLException{
    Connection conn=psql.conectar();
    Statement stmt=conn.createStatement();
    ResultSet rs = stmt.executeQuery(sql);
    String es="";
    while (rs.next())
        es=rs.getString(campo);
    
    return es;
    }
