    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int num = Integer.parseInt(jTextField1.getText());
        try {
            ResultSet rs =psql.consulta("SELECT id_cliente,importe,descripcion,mes FROM factura WHERE factura.id_cliente='"+num+"' ORDER BY mes;");
            Vector tit=new Vector();
            tit.add("Nombre");
            tit.add("Apellido");
            tit.add("importe");
            tit.add("Descripcion");
            tit.add("Mes");
            
            Vector data= new Vector();
            while(rs.next()){
             Vector row= new Vector();
            row.add(consulta(("SELECT nombre FROM cliente WHERE id_cliente='"+num+"';"),"nombre"));
            row.add(consulta(("SELECT apellido FROM cliente WHERE id_cliente='"+num+"';"),"apellido"));
            row.add(rs.getString("Importe"));
            row.add(rs.getString("descripcion"));
            row.add(rs.getString("mes"));
            data.add(row);
           
            }
            DefaultTableModel model = new DefaultTableModel(data,tit);
            jTable1.setModel(model);
       } catch (SQLException ex) {
            Logger.getLogger(Listafacturas.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

